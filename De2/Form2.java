    private void bntThemNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntThemNVActionPerformed
        // TODO add your handling code here:
        if (KiemTraRong() == true) {
            JOptionPane.showMessageDialog(null, "ban chua nhap du thong tin");
            return;
        } else {
            NhanVien nv = new NhanVien(tfHoten.getText(), tfNgaysinh.getText(), tfDiachi.getText(), nam.isSelected() ? "nam" : "nu",
                    cbb.getSelectedItem().toString(), Float.parseFloat(tfHesoluong.getText()), Integer.parseInt(tfThamnien.getText()), Float.parseFloat(tfLuongCB.getText()));
            nv.HienThi();
            JOptionPane.showMessageDialog(null, "them thanh cong");
        }
    }//GEN-LAST:event_bntThemNVActionPerformed

    private void btnLuuNVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuNVActionPerformed
        // TODO add your handling code here:
        if (KiemTraRong() == true) {
            JOptionPane.showMessageDialog(null, "chua du thong tin");
            return;
        } else {
            NhanVien nv = new NhanVien(tfHoten.getText(), tfNgaysinh.getText(), tfDiachi.getText(), nam.isSelected() ? "nam" : "nu",
                    cbb.getSelectedItem().toString(), Float.parseFloat(tfHesoluong.getText()), Integer.parseInt(tfThamnien.getText()), Float.parseFloat(tfLuongCB.getText()));
            try {
                FileWriter fw = new FileWriter("nhanvien.dat", true);
                BufferedWriter bw = new BufferedWriter(fw);
                bw.append(nv.HoTen + "$" + nv.NgaySinh + "$" + nv.DiaChi + "$" + nv.GioiTinh + "$" + nv.getPhongBan() + "$"
                        + nv.getHeSoLuong() + "$" + nv.getThamNien() + "$" + nv.getLuongCB());
                bw.newLine();
                bw.close();
                fw.close();
                JOptionPane.showMessageDialog(null, "luu file thanh cong");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "luu file that bai");
            }
        }
    }//GEN-LAST:event_btnLuuNVActionPerformed

