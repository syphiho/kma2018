    public boolean KiemTraRong() {
        if (tfHoten.getText().equals("") || tfNgaysinh.getText().equals("") || tfDiachi.getText().equals("")
                || tfHesoluong.getText().equals("") || tfThamnien.getText().equals("") || tfLuongCB.getText().equals("")
                || (!nam.isSelected() && !nu.isSelected())) {
            return true;
        }
        return false;
    }

